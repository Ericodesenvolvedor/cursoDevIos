public class Calcular_Media {
    public static void main(String[] args) {
        int nota01 = 10;
        int nota02 = 7;
        int nota03 = 4;
        int nota04 = 5;
        float tirarMedia = (nota01 + nota02 + nota03 + nota04) / 4;
        System.out.println("A média é: " + tirarMedia);
    }
}
