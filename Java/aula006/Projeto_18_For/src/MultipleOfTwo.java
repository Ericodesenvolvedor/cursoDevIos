public class MultipleOfTwo {
    public static void main(String[] args) {
        System.out.println("================CONTAGEM MULTIPLOS DE 2==================");

        for (int count = 0 ; count < 1000; count +=2) {
            System.out.println("Multiplo de 2: " + count);
        }

        System.out.println("================FIM CONTAGEM...==================");
    }
}
